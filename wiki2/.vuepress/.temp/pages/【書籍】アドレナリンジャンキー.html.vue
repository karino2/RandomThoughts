<h3 id="意外とパターンじゃない" tabindex="-1"><a class="header-anchor" href="#意外とパターンじゃない"><span>意外とパターンじゃない</span></a></h3>
<p>最初の方を読むと、この本は「プロジェクトに良くある性質とか傾向に名前をつけてまとめたもの」というふうに見えるし、
そういう本だと自称もしている。
だがそうでも無いものが思ったより多い。</p>
<p>例えば「オフショアの愚行」はオフショア意外と難しいよ、という話が書いてあるのだが、
良くあるなにかを記述していない。しかも内容も「アイコンタクト」と同じような内容で、離れた所にあるチームは上手く行かない、と言っているだけに思う。
なんかこういう、意外とパターンになってない事が多くて、そういう奴は単に著者の思い込みが書いてあるだけという印象で、
面白くも無い。</p>
<h3 id="読み物にはなっているが具体的ではないか" tabindex="-1"><a class="header-anchor" href="#読み物にはなっているが具体的ではないか"><span>読み物にはなっているが具体的ではないか</span></a></h3>
<p>面白い読み物にはなりそこなっている部分も多いけれど、説教が並んでいる<a href="./【書籍】情熱プログラマー.html">【書籍】情熱プログラマー</a>などよりは読み物として読める。
あまり説得力のない著者の主張みたいなのがたびたび出てくるけれど、それ以外の部分もそれなりにある。
読み物として読む事はできた。</p>
<p>だが、<a href="./【書籍】プログラミングの心理学.html">【書籍】プログラミングの心理学</a>に比べるとエピソードが具体的でないせいで、こう「あるある！そういうの！」という感じがあまり出ていなく、
その辺はソフトウェア開発エッセイとしての出来は微妙かな、と思う。</p>
<p>でもまぁ駄作のなろうを読む時間にこれを読んでも、まぁいいかと思える程度の内容ではあった。</p>
<h3 id="アジャイルの解説みたいなのは全く役に立たない" tabindex="-1"><a class="header-anchor" href="#アジャイルの解説みたいなのは全く役に立たない"><span>アジャイルの解説みたいなのは全く役に立たない</span></a></h3>
<p>なんか出来損ないのアジャイルの解説みたいなのがいくつかある。例えば「変更の季節」とか。
こういうのは出来が悪くて面白くもなければ勉強にもならない。
なんか十分な経験のもとに語るなにかじゃなくて、
本当にハウツー本みたいな程度の薄っぺらい内容になってしまっている。
2004年とかの本ならそこに新しさもあるかもしれないが、2008年とかでこんな内容…
と思ってしまうし、それを2023年に読むと本当に何も価値を感じられない。</p>
<p>こういうのは無くしてほしかったな、と思う。</p>
<h3 id="出来のばらつきが結構ある" tabindex="-1"><a class="header-anchor" href="#出来のばらつきが結構ある"><span>出来のばらつきが結構ある</span></a></h3>
<p>感覚的に、4割くらいがいまいちな内容。3割くらいは結構良い。残り3割が可もなく不可もなし、という感じに思う。
自分としてはハズレがちょっと多すぎるかな、という気もするが、まぁこんなもんと言われればそうかもしれない、
と思う程度ではある。</p>
<p>あんまり出来の悪いものにイライラせずにさっさと次に行くのが良い気はする。</p>
<h3 id="別段学びも無ければ読む必要は無い本" tabindex="-1"><a class="header-anchor" href="#別段学びも無ければ読む必要は無い本"><span>別段学びも無ければ読む必要は無い本</span></a></h3>
<p>何かソフトウェア開発に関する教訓のようなものとかは期待出来ない本に思う。
良いと言っているパターンも良いかどうか微妙だし、悪いと言っているパターンも悪いかどうか怪しかったりする。
あまり根拠とかも無いし説得力もあまり感じない。
なにかプロジェクトマネージメントとかを学ぼうという対象としてはあまり良くないように感じた。</p>
<p>なんか昔ピープルウェアとか読んだ時は結構得るものも多かった気がするんだが、
この本はそういう要素は全然無いね。</p>
<p>ただ説教臭いのにうんざりして読み始めた本なので、そういう要素が無い方が望んだものなのかもしれない、とは思う。
だから学びが無いのはマイナス評価という訳では無い。</p>
<h3 id="読んで思う事はそれなりにあった" tabindex="-1"><a class="header-anchor" href="#読んで思う事はそれなりにあった"><span>読んで思う事はそれなりにあった</span></a></h3>
<p>読んでいると反発にせよなんにせよ、自分の考えを刺激するような部分はあった。
このページの感想がそれなりの量だ、という話。
なにか技術的な事についてブログとか書きたいなぁ、でも別段書きたい事無いなぁ、
という時に読むと良いかもしれない。
なにかを書こうという気分にはなる。</p>
<p>そういう思考を刺激するものとしては一定程度機能していたと思う。</p>
