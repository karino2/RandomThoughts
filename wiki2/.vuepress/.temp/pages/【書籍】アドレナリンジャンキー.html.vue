<h2 id="死んだ魚" tabindex="-1"><a class="header-anchor" href="#死んだ魚"><span>死んだ魚</span></a></h2>
<p>なんかこのエピソードは良く分かるなぁ。自分も腐臭がする、と同じような事を表現した事がある。
遅れると最初から分かっていて何も言わないだけじゃなくて、
マネージャーが死んだ魚の目をして支離滅裂な事を言ってくるというのも特徴としてある気がする。
死んだ魚の目、という自分の比喩と死んだ魚というこの本の比喩の共通点もちょっとおもしろい。</p>
<h2 id="_9まで読んだ範囲で、この本はどういう本か" tabindex="-1"><a class="header-anchor" href="#_9まで読んだ範囲で、この本はどういう本か"><span>9まで読んだ範囲で、この本はどういう本か</span></a></h2>
<p>全部で80ちょいのエピソードがあるようで、現在9まで読んだ。その範囲でこの本はどういう本かを書いてみる。</p>
<p>この本は、チームやプロジェクトの良く見かけるパターンを名前をつけて並べたもの、といえる。
うまく行っているプロジェクトのパターンもあれば失敗しているプロジェクトのパターンもある。
うまく行っているプロジェクトが本当に良いパターンか、というのは微妙な事もあるけれど、
<a href="./【書籍】情熱プログラマー.html">【書籍】情熱プログラマー</a>のようにどうすべきか、という事を言うのではなく、
うまく行ってない感じを説明して終わりにするので説教臭さは少ない。</p>
<p>この本はチームとかプロジェクトが対象で、個人のプログラマは対象では無い。プログラミングもあまり対象では無い。
ソフトウェア開発、という感じに思う。
また、最近読んでいた本と同様に、やはりそれなりの大きさの会社組織が前提になっていて、フリーランスや零細ベンチャーとはだいぶ状況は違う。</p>
<p>ただ今のところは読み物としてそれなりの面白さがある。そうそう、こういうのでいいんだよ。</p>
<h2 id="「一人一役」は良いんだろうか" tabindex="-1"><a class="header-anchor" href="#「一人一役」は良いんだろうか"><span>「一人一役」は良いんだろうか？</span></a></h2>
<p>20の「一人一役」というのは、各メンバの仕事の責任が明確に分かれていて、それぞれが自分の専門の作業をこなす、
という形態のチームを指していて、これは上手くいくチームであるとなっている。
それと対比されているのが「全員がすべてのことに責任を負う」というスタイルで、
これはめったにうまく行かない、といってどうして上手く行かないかをレストランの役割分担にたとえている。</p>
<p>自分の経験では責任が明確に分かれている方がうまくいくという気はあまりしない。
分かれていると分かれているがゆえの非効率さはあるし、またそれ故に人数がより多く必要になる傾向があると思う。
皆が全部の事をやる方が小さなチームでは良く見られる形式だし、小さなチームではそれゆえに上手くいく事も多い。</p>
<p>以前に比べて昨今は独立したテストチームとか運用チームを持たない事が多く、
それはなるべく横断的に作業出来るように、という事だと思うんだよな。
それがいつも上手くいく訳では無いけれど、上手くいく事はそれなりにあるように思う。</p>
<h2 id="開発形態が古い気がする" tabindex="-1"><a class="header-anchor" href="#開発形態が古い気がする"><span>開発形態が古い気がする</span></a></h2>
<p>要求があってQA担当が居て製品を作ってテストする、みたいな形式の話が多く、
そこで書かれている事がどうも自分が新卒だった頃の会社を思い出す。
あれは自分の中では2000年代の初頭から2005年くらいまでの時代を反映しているが、2000年代の後半もある程度はそういう組織はあった気もする。</p>
<p>だがそれでも本書が書かれているのは原著が2008年と言っている。この頃ならもうちょっとそういう形態では無くなっていたような？</p>
<p>昨今は独立したQA担当というのはあまり居ない気がする。大規模組織ではバカでかいCIを作り込む専用のチームはあるけれど、
テスト自体は各自が追加する。
そして製品自体のテストは以前よりも劣化した。
その代わり問題が出た時に迅速に直すようになったし、リリースも段階的に行われるようになった。</p>
<p>みんながみんなそう上手くこのスタイルを用いている訳では無いけれど、うまく行ってない組織も以前のようにやっている所は少ない。</p>
<p>その結果、QAと開発の軋轢みたいな話や形式チェックばかりしてしまう、みたいな話は、
行く所に行けばあるとは思うがそんなに多くある話では無くなったような気がする。
実際いろいろな職場を渡り歩いたが、ここ10年くらいそういうのを目にした事は無い。</p>
<p>2008年だったらもうちょっと現代的な話が多くてもいいんじゃないかな？という気がするんだが。
現代的なスタイルには現代的スタイルなおもしろエピソード、いろいろあると思うんだけどねぇ。</p>
<h2 id="アトラスはそんなに悪いだろうか" tabindex="-1"><a class="header-anchor" href="#アトラスはそんなに悪いだろうか"><span>アトラスはそんなに悪いだろうか？</span></a></h2>
<p>マネージャーが全部他のチームとの折衝やマネージメント的な作業をやってしまう為、他のリーダーが育たず25人くらいまでしかスケールできない、けれどその範囲では非常に優秀、というようなものをアトラスと呼んでいて、良くない事としているが、そんな悪いかね、これ。</p>
<p>100人が必要な仕事にはスケールさせられないので大きな仕事は任せられない、と書いてあるが、規模を増やさずにより重要でインパクトの大きい仕事を任すという方向への進歩はありえると思うんだよな。
大きいものを大人数で作るよりも、iPhoneを作って世界を変える方が重要なんじゃないか。</p>
<p>このリーダーが居なくなったあとが困るという話も、25人というのが多すぎるという話でもう少し小さいチームであれば同じスタイルのリーダーを持ってくれば済む話で、そこまで問題も無さそうに思う。</p>
<p>マネージャーになりたい人もいるはずだ、と言っているが、みんながこのリーダーの下で働きたがるという冒頭の話からすると、マネージャーの仕事を嫌がっている人たちが集まっていると考える方が自然では無いかなぁ。</p>
<p>むしろこういうチームがたくさんになるように組織を変えていくのは、結構現代的なマイクロサービス的チームになって良さそうではなかろうか。</p>
<h2 id="ピアプレビューはもはや一般的" tabindex="-1"><a class="header-anchor" href="#ピアプレビューはもはや一般的"><span>ピアプレビューはもはや一般的</span></a></h2>
<p>採用でマネージャー以外のチームのメンバも面接するのをピアプレビューと呼んでいて一般的では無いが有効だ、
という書き方をしているが、今となっては普通だよな。
この頃の時点でもまぁまぁ一般的だった気がするが…</p>
<p>ワインバーグの本でもそうだが、コンサルが書くとコンサルが対象とするような企業というバイアスが結構ある気がする。
例えば零細はコンサルなんて雇わないよな。
モダンである事を自慢にしている中堅優良ベンチャーなどもあまりコンサルは雇わない気がする。
コンシューマー向けの一般的なwebサービスの企業もあまり雇わない気がする。
コンサルを雇うような企業というのは、自分が働かないような企業が多い気がするな。
どちらかといえば自分が仕事をもらうような企業を対象とした話が読みたい所だが…</p>
<h2 id="最近はどうなったのかを考えてみる" tabindex="-1"><a class="header-anchor" href="#最近はどうなったのかを考えてみる"><span>最近はどうなったのかを考えてみる</span></a></h2>
<p>前にも言った通り、どうもこの本の前提となっている開発形態は古い気がする。
例えばイノベーションのところで「その一方で、どんなプロジェクトでも、完成する時期をかなりの精度で予測する必要がある。」とあるが、
これはかなり違和感のある主張に思う。
こうした違和感がちょくちょくあるので、逆にじゃあ現在と感じるのはどういうものなのかな？というのを考えてみたい。</p>
<p>自分が最近取り組むプロジェクトは、すでにリリースされているサービスやソフトウェアの変更が多い。
期限は別に無いし、リリースして反応を見てまた次の方向性を考えるので、リリースの前に合意を形成したり顧客の期待値をコントロールしたり、という事は必要無い。
作るものがどれだけ客に望まれているのかも良く分からない中でものを作るし、多くはそれほどのjustificationも無い。</p>
<p>これは現在のモバイルのアプリやwebのサービスでは割と一般的に思う。リリースは継続的に続いていくし、
顧客の反応も予想するよりは確認していくものだ。
本当に顧客がそれを望んでいるかは良く分からないし望んでいないものも実際たくさん作っていると思うしそれを避ける事は難しいという前提に立っていると思う。
それは、これまでよりもより本質的な正しいものの見方に思う。</p>
<p>予想に重きを置かずに、反応を見て修正していくというのは大きな違いに思う。
見積もりもあまり行わずにどちらにせよそんなに長く無い期間に終わるものを出して反応を見ていく。
「なるべく予想を避けて結果を見る」というのは現代的な手法の基本と言えそう。</p>
<p>これらのやり方は新規に大きなものを突然出すのには向かないけれど、なるべくそういう物を減らしているのが現在のやり方なんじゃないか。
現代のやり方で作るのに向いているものをみんな作っている、というか。
作るものに合わせてやり方ができたのか、やり方に合わせて作るものが定まっているのかはどちらか良く分からないし、
たぶん両方あるんだろうけれど。</p>
<p>マネージャーとか経営陣の立場もだいぶ違うように思う。マネージャーとか経営者は説得する対象という訳では無い。
彼らもやはりサービスやアプリを成功させたくて、むしろかつてそれを成功させるのに大きな貢献をした人がCTOなどになって組織の経営判断をしている。
彼らは自分たちが開発しているサービスを知らない訳じゃないし、むしろ良く知っている。
もっとユーザーが直接問題になっているよな。
ユーザーをたくさん獲得できれば成功、出来なければ失敗。</p>
<p>これはこの本に書いてあるような「客」とは違う。お金を出して自分の欲しいものをいう訳では無いし、
プロジェクトの協力関係にも無い。この本の「客」はお金を出した分、自分の望むものを作って欲しいという動機がある。
一方で昨今の「ユーザー」は良いものだったらやるが、良くないものならやらないだけで、後者が別に損害では無い。
もっと顔が見えない沼のようなものをかき混ぜてどうにかするようなイメージが近いんじゃないか。
手応えも反応も無い中でどうやって望みを知るのか、というのは現代的なソフトウェア開発の重要な部分で、
この本に無い事に思う。</p>
<p>現代のソフトウェア開発の大部分はすでにある程度成功したものを、さらに発展させていこうとするものに思う。
何も無い所で成功させる事は現代でも誰かがやっているはずの事だけれど、多数派はそうした事をやっていない。
現状ある程度成功しているソフトウェアに多くの人をつぎ込んでそれを発展させるというのが多数派のプログラマのやっている事だろう。
それを上手くやっているかどうかは怪しい部分もあるけれど、それは本質的に難しい事なのは真実と思う。
本質的に難しい事を、うまくやれているかどうかは良く分からないけれどやっている、というのが現状に思う。</p>
<p>このすでにある程度成功しているプロジェクトを継続的に発展させていく、という形態がほとんど出てこないのが、本書を読んでいてなんか実態と違う古いやり方に感じられる事じゃないかなぁ。
実際自分のここ10年の仕事を振り返っても、顧客を満足させる仕事はたぶん一回も無かった。
だからそれが主流って事は無いんじゃないか。今でもそういう仕事もあるとは思うけれど。</p>
<h2 id="最後まで読んでの感想-2023-01-15" tabindex="-1"><a class="header-anchor" href="#最後まで読んでの感想-2023-01-15"><span>最後まで読んでの感想 2023-01-15</span></a></h2>
