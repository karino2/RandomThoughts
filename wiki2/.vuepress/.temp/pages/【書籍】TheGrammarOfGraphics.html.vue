いまいち列挙するありがたみが分からない。
他にも普通の人は書かないようなへんてこな図（グラフ理論のグラフとややこしいので図と表記）を持ってきて、
これは距離空間になってない、と指摘する事に何か意味があるんだろうか？
感覚的な理解で十分な問題に対して過度に抽象的に扱って、無理やり抽象的に扱う意味がある例を絞り出しているようにしか見えない。
ほとんどの人が使わないグラフの意味を定義する事にこんなに紙面を使う意味はあるんだろうか？
多くの人が使う普通のグラフの意味や理解を深めるなら意味があると思うんだが…</p>
<p>なんか数学的な定義がいろいろあって読むの苦労する割には得るものが無いなぁ。
次の14章も同じ感じだったらもうちょっと読み飛ばしていこう。</p>
<h2 id="part-2の読み方" tabindex="-1"><a class="header-anchor" href="#part-2の読み方"><span>Part 2の読み方</span></a></h2>
<p>14章と15章もサラサラと読んでみて、ちょっとPart 2をどう読むべきかを理解出来てきた気がする。
Part 2の各章は、</p>
<ol>
<li>論理的な話（数学的な話）</li>
<li>心理学的、肉体的な受け止められ方の話</li>
<li>使われるグラフの具体例</li>
</ol>
<p>という構成になっているのだが、1と2の結果3になっている訳では無い。
1と2は現時点での理解がまとめられていて、その結果なんだ、という結論にはなっていない。
既存の論点や研究などを外観する程度の理解で良さそう。</p>
<p>そして3の具体例もそれほどシステマティックな話では無いので、関連するトピックがこんなふうに表出するんだな、程度で良いと思う。</p>
<p>1とか2を真面目に理解してもその分野を研究する気が無ければあまり意味が無い。
それよりは1と2でどういうトピックが扱われているかに集中して、中身自体はそれほど気にせずに進む方が良さそう。
3の具体例でもあまりそれの表す事などを考えずに、1と2の関わりを眺めるくらいでサラサラ先に進む方がいい気がする。</p>
<p>そういうふうに14章と15章を読んだら、割と言いたい事が理解出来た気がする。</p>
