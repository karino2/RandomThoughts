<h3 id="nestの復習" tabindex="-1"><a class="header-anchor" href="#nestの復習"><span>nestの復習</span></a></h3>
<p>nestが出てくるので見直す。p 64 5.1.2.2 Nestのあたりがその定義。
まず感覚的にはnestは/の右側でタグ付けするようなもので、グルーピングの一種と思われる。
それを念頭に、もうちょっと厳密な定義を見直しておく。</p>
<p>これらの定義はVarsetに対して行われている。varsetの定義はp30 2.1.9。
VからO~へのmappingのトリプレットをvarsetと呼ぶ。variableのinverse。
O~は複数のO~から同じVへのmappingがありうるので、個々の要素はbag of objectsとなる。</p>
<p>まずcrossから見ていく。まずVの方では、単純にproductとなっている。
一方でA<em>Bの方ではred, 5が存在しない。
A</em>Bは合成されたmappingを表していると思うので、Vとしては存在してもfが存在しないケースがある、という事だな。</p>
<p>一方でnestの方はVの定義が違う。Vが必ず、mappingが存在するものしか存在しない。
イメージとしてはcrossがdomainがproductであるのに対して、nestの方はproductの次元の間をくねくね移動する折れ線のようなもの、という感じだろうか。一次元とは限らないが、独立に自由には動けない。</p>
<h3 id="_11-2のnestとcrossの違い" tabindex="-1"><a class="header-anchor" href="#_11-2のnestとcrossの違い"><span>11.2のnestとcrossの違い</span></a></h3>
<p>以上を踏まえて11.2のnestとcrossの違いの説明を読む。
a<em>bが二次元だが、a/bは一次元と言っている。
これは結果のテーブルにといて、a</em>bが2x2で縦と横に並んでいるのに対して、
a/bが横一列に並んでいる事を言っているのだと思う。</p>
<p>J, MとB, Jが独立じゃないので、２つをセットにインデックスづけされていると考える訳だよな。
それはまた、JとMでカラーリングされているが、B, J, Bであるとも考えられるべき、という事なのだろう。</p>
<p>そのあとの結婚とgenderの組み合わせと妊娠とgenderのくみあわせの話は分かりやすい。
結婚は男女ともしうるのでcrossだが、妊娠は、男性は妊娠してない、の一択となる。
だから (woman, preg), (woman, nonpreg), (man, nonpreg)の３つと考えるべきで、
manとwomanを並列に並べてpregとnonpregをcrossさせると考えるのは適切じゃない、という事だな。</p>
<p>crossでmappingが存在しないケースは観測されていないだけと考えるが、nestではそもそもそういうケースが論理的に存在しない、との事。この区別も分かりやすいな。なるほど。</p>
