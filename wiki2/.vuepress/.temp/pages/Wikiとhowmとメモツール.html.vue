<h2 id="howm的とは" tabindex="-1"><a class="header-anchor" href="#howm的とは"><span>howm的とは</span></a></h2>
<p>howmは昔あったemacsのメモ環境で、その場の日付でメモを作っていって見る時は検索で見る、という感じのコンセプトのツール。
こういうものをここではhowm的と呼ぶ事にする。
それがどういうものかを簡単に説明する。</p>
<p>まずメモを作るのに名前を考えたりとかする必要は無く、その場でメモを書いて保存するだけ、というもの。
メモを作るコストとしてはこれがほぼ最小と言って良いと思う。</p>
<p>一方で書いたメモを後から見る時は、検索で探す感じになる。</p>
<p>同じキーワードでひっかかったメモを連結して表示、などの機能があって、これを使って複数のメモをまとめてページのように見る事が出来る。</p>
<p>自分の最近のツールでは<a href="./MDDeck.html">MDDeck</a>や<a href="./てきすとでっき.html">てきすとでっき</a>は似た系列と言える。</p>
<p>コンセプトとして書くコストを最小限にして、その分見る側に少しコストを課すような物になっている。</p>
<h3 id="利点-メモを書くのが楽" tabindex="-1"><a class="header-anchor" href="#利点-メモを書くのが楽"><span>利点：メモを書くのが楽</span></a></h3>
<p>とにかくメモを書くのがかったるくない。小さな事でも書ける。
書かれないメモは後で見られる事は絶対にないので、書かれる、というのは大きなメリットに思う。</p>
<p>最近では買い物メモなどのように短期間でいらなくなるもの、<a href="./腕時計.html">腕時計</a>などの買ったグッズなど、
一旦書いたらしばらく視界から消えていいが、何年後かに見たくなったりする事が稀にあるものなどはこの形式の方が手軽に思う。</p>
<h3 id="利点-カテゴリが良くわからないメモを書きやすい" tabindex="-1"><a class="header-anchor" href="#利点-カテゴリが良くわからないメモを書きやすい"><span>利点: カテゴリが良くわからないメモを書きやすい</span></a></h3>
<p>複数のPCをまたいだ仕事の作業メモなどもこういう形式の方がいい。
例えばWindowsでのパフォーマンスの計測結果を蓄積しつつ、その結果をうけてMacでのパフォーマンスを計測していく、みたいなもの。
会社のissue trackerでもいいんだが、変なVPNの中にあってVPNは一台しかつなげない、とかそういう事情があったので、ローカルにメモを残したかった。
こういう一時的にメモを両方でためていってある程度結論が出たらissueにまとめて、その後は二度と見ない、みたいなものにいちいちWikiNameを考えるのはかったるい。</p>
<h3 id="利点-勝手に見えなくなる" tabindex="-1"><a class="header-anchor" href="#利点-勝手に見えなくなる"><span>利点: 勝手に見えなくなる</span></a></h3>
<p>howm的なものの良い所は、勝手に流れていって視界から消えるので、消す手間がないというのはあると思う。
買い物のメモなどは、買い物が終わったら消えていい。
でもいちいち消すのは面倒だ。</p>
<p>しかも消した後にちょっと見たくなる事がたまにある（あれ？ドレッシングはこの前買ったっけ？とか）。</p>
<p>消すという作業をせずに勝手に流れていって見えなくなるのはその辺ちょうど良い塩梅である。</p>
<p>また勝手にすぐ見えなくなるというのは、どうでもいい事を安心して書き散らかす事が出来る。
ブログの更新が億劫だけどtwitterでは良くつぶやくのに似ていると思う。</p>
<h3 id="欠点-webの公開と相性が悪い" tabindex="-1"><a class="header-anchor" href="#欠点-webの公開と相性が悪い"><span>欠点: webの公開と相性が悪い</span></a></h3>
<p>検索というのはgithub pagesにそのまま出すのとは相性が悪い。
サーバーサイドにそういうサービスを作る事は出来るが（かつてRandomNotesだっけ？そういうサービスがあってなかなか良かった）、
github pagesにそのまま公開する事は出来ない。</p>
<p>また全てをフラットに保存して見る時に検索で見る、という形式なので、公開と非公開を安全に分けるのも難しい。
同じフォルダを対象にスクリプト処理をすると、どうしても何かのミスで公開されるリスクがある。</p>
<p>見え方というのが見る時に初めて決まる類の思想なので、どう見せるか、というのは自然には出来上がらないので、
見せる事は別に考えないといけない。</p>
<h3 id="欠点-流れていく" tabindex="-1"><a class="header-anchor" href="#欠点-流れていく"><span>欠点: 流れていく</span></a></h3>
<p>これは利点でもあるのだけれど、メモの中には流れていって欲しいものとそうでない物がある。
流れていってほしくないものもある程度たったら流れていってくれていいんだけど、
howm的なメモはどうでもいい大量のメモが発生する結果、ちょっと残って欲しいものもすぐに流れてしまう。</p>
<h3 id="欠点-あとから構成を変更する事が出来ない" tabindex="-1"><a class="header-anchor" href="#欠点-あとから構成を変更する事が出来ない"><span>欠点: あとから構成を変更する事が出来ない</span></a></h3>
<p>write onlyなものに向いているとうか、そのファイル内を変更する事は出来るのだが、
だんだんとあとから構造を深化させていく、みたいな事がやりにくい。
書き散らす事は出来るのだが、そこの次になかなか進まない。</p>
<h3 id="欠点-昔の周辺情報がわかりにくい" tabindex="-1"><a class="header-anchor" href="#欠点-昔の周辺情報がわかりにくい"><span>欠点: 昔の周辺情報がわかりにくい</span></a></h3>
<p>当時書いていたメモの周辺にどういうメモを書いていたのか、みたいな事が、時間が経った後に見直すとわかりにくい。
これは完全にわからない訳ではないのだけれど。</p>
<p>Wikiのいい所はすごい昔に書いていた事も、久しぶりに見ていくと当時と同じ姿で見えるので、
思い出しやすいし続きも書きやすい。</p>
<h2 id="wiki的なもの" tabindex="-1"><a class="header-anchor" href="#wiki的なもの"><span>Wiki的なもの</span></a></h2>
<p>自分のメモは現状は<a href="./TeFWiki.html">TeFWiki</a>に残す事が一番多い。
これは講義のノートなどには非常に上手くいっているし、その他にもかなり多くの分野でうまくメモを残せている。</p>
<p>howm的なものに比べて、書いたり整理するのに少しコストが掛かるが、
その代わりに後から見る時には見やすい。</p>
<h3 id="欠点-wikinameを考えるのが面倒" tabindex="-1"><a class="header-anchor" href="#欠点-wikinameを考えるのが面倒"><span>欠点: WikiNameを考えるのが面倒</span></a></h3>
<p>ちょっとしたグッズをいちいちWikiNameを考えるのが面倒。
また、買い物するまで残しておきたいが、買い物が終わったら消えてもいい、みたいなものなど、
カテゴリを考えるのが面倒なメモみたいなものをいい感じに書いていけない。</p>
<h3 id="欠点-消えていかないのでどうでもいい事が書きにくい" tabindex="-1"><a class="header-anchor" href="#欠点-消えていかないのでどうでもいい事が書きにくい"><span>欠点: 消えていかないのでどうでもいい事が書きにくい</span></a></h3>
<p>どうでもいい事をとりあえず書くと、それがずっと残ったりしていまいち。
見えやすさがアクティブさと一致しないので、もう過去になったものがずっと視界に入ったりする。</p>
<h2 id="折衷案を考える" tabindex="-1"><a class="header-anchor" href="#折衷案を考える"><span>折衷案を考える</span></a></h2>
