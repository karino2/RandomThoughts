この章は他の章よりはずっとまともな書き方ではあるけれど、それでもやはり医療に関わる文章を書くには問題のある書き方ではあるので。</p>
<p>話を戻して。多様な微生物を多量に持ってこないといけない、となると、効果が良く分からないので難しさがあるよな。
