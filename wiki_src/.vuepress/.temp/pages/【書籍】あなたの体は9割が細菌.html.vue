<h2 id="読み終わった感想、酷い本だったので読まない方が良い" tabindex="-1"><a class="header-anchor" href="#読み終わった感想、酷い本だったので読まない方が良い"><span>読み終わった感想、酷い本だったので読まない方が良い</span></a></h2>
<p>腸内細菌が健康に与える影響はいろいろと聞き及んでいて、食物繊維はかなり摂っている方の自分としては、
腸内細菌の働きについて分かっていることや分かっていないことがいろいろ知れるのでは無いか、と期待して読み始めたが、完全に期待はずれだった。
この著者は分かっていることと分かっていないことを記述する能力が無い。
だから読んでも何が分かっていることで何が分かっていないことなのか判定出来ない。</p>
<p>主に以下のことについて述べていて、</p>
<ul>
<li>肥満に対する影響</li>
<li>抗生物質の影響</li>
<li>自閉症への影響</li>
<li>腸の透過性と免疫への影響</li>
<li>経膣出産や母乳育児の影響</li>
</ul>
<p>何か根拠があるのだろう、とは思うが、本書からそれを知るのは難しい。
相関しか無いもので「原因なのは明らかだ」とか言って、その上にさらにいろんな論を同じ調子でつみたてていくので、
どこが著者の思い込みでどこが分かっていることなのかを、後半に行くほど判定ができなくなる。
だから読者としては全部疑うしかなくて、情報として得るものが無い。</p>
<p>[[【書籍】本当に役立つ栄養学]]などでは腸内細菌の話がもっとまともに扱われていて、もうちょっと詳しく知りたいな、と思っていたのだけれど、
この本では駄目だった。同じテーマのもっとまともな本が読みたい。</p>
<p>この本は、著者は否定しているが、典型的な代替医療の本になっていて、
既存の治療を薄弱な根拠で否定し続ける。
例えば抗生物質の使用を否定している訳では無い、
といいつつ、その弊害では根拠がほとんど無いようなものを延々と恐怖を煽る調子で書き続ける。
抗生物質以外でも、昔の人類に無かったものはみんな似たようなものだ。
これは著者が本当に「明らかだ」と思いこんでしまっているのだろう。</p>
<p>分かっていることをいろいろ教えてくれるだけで良くて、「こうに違いない！」とかいう良くわからない思い込みを並べないで欲しいんだがなぁ。</p>
<p>個人的には証拠が無くても自然派でいるという選択をするのは別に良いと思う。
科学的に分かることは限られているので、信仰として分かっていないものに対しての態度を決めるという話だと思う。
ただそれを正当化する為に科学のフリをするのはよろしく無いと思うし、
ましてやそうした話を自分は読みたいとも思わない。
これを読んで帝王切開や粉ミルクの育児や自閉症の子供の子育てについて間違った思い込みをしてしまうのは凄く有害だと思うので、
この本は読まない方が良いと思う。</p>
