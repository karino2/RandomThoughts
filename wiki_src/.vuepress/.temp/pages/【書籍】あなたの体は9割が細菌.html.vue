<li>量的な情報を省かない</li>
<li>出展を明記する</li>
<li>数値を出す時には、それを出した実験の前提をちゃんと示す</li>
<li>疑わしい根拠で単一の簡単な理由を強く主張しすぎない</li>
</ul>
<p>著者はそれでも科学的な態度のつもりでは居るらしく、そういう言及は何回かあるけれど、抗生物質批判でも帝王切開批判でも、科学的な話からは遠く離れてしまい、
