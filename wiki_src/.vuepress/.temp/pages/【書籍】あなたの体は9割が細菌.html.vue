簡単に説明出来て欲しいという強い心理的圧力が働いているもとでこういうものを見ると、
ますますそれが正しいと思ってしまう。</p>
<p>この悪循環を断ち切るためには、誤って分かると思い込んでしまうところにブレーキを掛けるメカニズムが必要で、
二重盲検法で分かっていないことはちゃんと分かってはいない、というところを絶対に死守するという態度が、
もっとも汎用的に使えて、多くの人が納得しやすいブレーキのメカニズムなんじゃないか。</p>
<p>そこを妥協してしまうと、誤った思い込みのブレーキのメカニズムが無くなって、どんどん変な思い込みが強くなってしまう。</p>
