<p>なぜ科学的な態度を軽視するとそうなってしまうのか、というのをこの本は示しているんじゃないか。</p>
<p>特に人体の健康というのはとても複雑な要素で出来ているため、いろいろな相関を見出すことは容易に出来てしまう。
だから自分に都合の良い相関を探すのが、他の分野より簡単だと思う。</p>
<p>また、健康というのは深刻で重要な問題であるので、関心が高くて、しかも切実に解決を望む人が多いという特徴がある。
けれど生物は複雑なので、切実に解決をしたい問題の原因が良く分からないということが良く起こる。
この、なんとかしたいけれど原因が良く分からない、というのは凄く心理的にストレスな現実なので、
簡単なメカニズムで説明したいという強い心理的欲求が生まれる。</p>
<p>良くあるのが昔の人間との差分を考えることで、原因が理解出来ると思い込む、というようなもの。
