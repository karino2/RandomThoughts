健康というのは深刻ゆえに、「なんか怖い」みたいな気持ちは増幅されがちに思う。
そして増幅された気持ちを本当か調べてみると大量の相関がいつも簡単に見つかって、ますますその怖さを煽ってしまう。</p>
<p>分からないものの恐怖を煽るのは、有害であることが多い。
素人に分からないけれど使われているものの多くは、有用であるから使われているのであって、
さらにその中には十分に二重盲検法で確認が出来ているものも多い。</p>
<p>示されていないものは分からない、という状態に留めておき、示すためには二重盲検法を死守する、
という姿勢は、やはりこのなんだから分からないけれど怖い、というのが増幅されてしまって、
恐怖からますます変な思い込みに走ってしまう問題のブレーキとして、汎用的につかえて皆が納得出来るものなんじゃないか。</p>
<h2 id="_8章、糞便移植やプロバイオティクスなどの話は突然まともに" tabindex="-1"><a class="header-anchor" href="#_8章、糞便移植やプロバイオティクスなどの話は突然まともに"><span>8章、糞便移植やプロバイオティクスなどの話は突然まともに</span></a></h2>
<p>8章は突然内容も書き方もまともになる。これまでは変な相関だけで「粉ミルクが肥満を生んでいるのは明らかだ！」とか言い切ってたのに、
この章だけ因果関係が示せてないのでは？と思う所は因果関係は不明と書かれていることが多い。
実際に行っている医療従事者などからまともな話を聞く機会が増えるからだろうか。
最初からこういう話を書けばいいのにねぇ。</p>
<p>ただやはり難病とかの治療について書くなら、こういう基礎的な科学的な態度に欠ける著者にやらせるのは良くないよなぁ、と思う。
