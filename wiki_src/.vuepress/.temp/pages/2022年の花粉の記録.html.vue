<p>今年はすごく症状が軽かったが、それについて考えられる事を列挙しておく。</p>
<ul>
<li>今年は飛散量自体が少なかった。ピークの日の飛散量は例年並みだったが、合計の飛散量は少なかった</li>
<li>今年は配食サービスとBASE FOODの結果、家からほとんど出なかった（買い物や外食に出る事が無かった）</li>
<li>去年苦しんだ目の異物感は、部屋の掃除で抑える事が出来た</li>
<li>シーズンの最初から空気清浄機を使った初めてのシーズンだった</li>
<li>デザレックスはかなり有能かもしれない</li>
</ul>
<p>以下詳細も。</p>
<h3 id="今年はそもそも飛散量が少なかった" tabindex="-1"><a class="header-anchor" href="#今年はそもそも飛散量が少なかった"><span>今年はそもそも飛散量が少なかった</span></a></h3>
<p>今年は飛散の初期に割と大量飛散の日があって、そのままピークが来るのかと思ったがそうでは無く、
その後10日おきくらいに散発的に大量飛散の日がある、という感じで、
予報を見て「ピークが来る」と毎日言われて警戒していたが、
そのままズルズルとピークが終わるまで来てしまった感じだ。</p>
<p>例年より警戒していた期間が長いが、全体としては飛散量が長期予報よりも大きく少なかった、というのは結論づけて良さそう。</p>
<p>ただピークの飛散量は例年とあまり変わりないレベルなので、
大量飛散の日に症状が出なかったのは何か理由があるのかもしれない。</p>
<p>累積の飛散量がそれほどじゃなくてまだ今シーズンの許容限界みたいなのに達していない可能性はある。</p>
<h3 id="家から出ない大切さ" tabindex="-1"><a class="header-anchor" href="#家から出ない大切さ"><span>家から出ない大切さ</span></a></h3>
<p>家から出ないのが思っている以上に重要だというのが今年の発見に思う。</p>
<p>今年唯一いつもと同じように症状が出た3/27は、サーフィンに出た日だ。
それなりに飛散量が多い日でもあったが、一番多い日という訳では無かった。
それでもこの日は一日症状に苦しむ事になった。</p>
<p>外に出ると良くない事は知っていたが、部屋に戻ったあとがこんなに違うというのは、一切外に出ない生活をして初めて知った事である。</p>
<p>例年もあまり外に出ないようにしてピークの時間は外出を避けていたが、それでも全く外に出ない訳では無かった。
でも今年は[[ステッパー]]を買って家での運動環境も整えて、[[配食サービス]]と[[BaseFood]]で完全に家から出ない生活をしていた。</p>
<p>一度症状が出た後に部屋に戻っても症状は収まらなく、一日症状は続く。
だが、一度も外に出なければ症状は出ない。
どうも一度外に出るとその日は家に帰ったあともダメという事らしい。</p>
<p>これはたぶん室内にそれなりの花粉が飛んでいて、けれど症状が出るほどでは無い状態なのでは無いか。
一旦症状が出ると室内の花粉のレベルでも反応してしまって症状が一日収まらないが、
症状が出てない状態なら平気なのだろう。
この一度症状が出ると収まらないが、最初から症状が出ない状態をキープすれば平気、というのは大きな発見だ。</p>
<h3 id="去年の後半出た目の異物感は掃除の問題だった" tabindex="-1"><a class="header-anchor" href="#去年の後半出た目の異物感は掃除の問題だった"><span>去年の後半出た目の異物感は掃除の問題だった</span></a></h3>
<p>去年は目の異物感が後半からひどくてひどい目にあったが、花粉のシーズンが終わってもおさまらずに色々対策をしたら、部屋の掃除で収まる事を発見した、という経緯がある。
ただアレルギー検査したらハウスダストは無くてスギ花粉が出たので、この辺は良く結論が出ていない。
今年もピークにはまたあの症状が出るのでは？と少し恐れていたが、最後まで掃除で収まる範囲でしか異物感は出なかった。</p>
<p>シーズンが始まると少し異物感を感じたので、やはり杉花粉と部屋のホコリの相乗効果で起こる現象ではありそう。
ただ掃除を頻繁に行うと症状は収まり、それはピークの日でも変わらなかった。
今年もこの症状が出たら八丈島でも行こうと計画を立ててたくらい恐れていたが、
結果としては最後まで掃除で治まった。
なお去年は掃除機とコロコロを両方やりまくっていたが、今年は掃除機は週一くらいでコロコロ主体にしてみて、
これでも十分治まっていた。</p>
<p>もっと飛散量が多い年でも本当に治まるかはまだ不明だが、現時点では頻繁なコロコロをしていれば大丈夫と思っておいて良さそう。</p>
<h3 id="空気清浄機が効いている可能性" tabindex="-1"><a class="header-anchor" href="#空気清浄機が効いている可能性"><span>空気清浄機が効いている可能性</span></a></h3>
<p>去年のシーズンの後半から導入した空気清浄機。
これが効いているのかどうかは分からないが、去年との違いの１つではあるし、何か効いているような気もしている。
ただ3/27はこれでも治まらなかったので、
あくまで症状を弱める程度で解決するほどでは無いんじゃないか、という気がしている。</p>
<p>実はプラセボだったと言われても驚きは無いが、実は結構効いていたと言われても驚きはない感じ。
来季以降も使っていきたい。</p>
<h3 id="デザレックス有能" tabindex="-1"><a class="header-anchor" href="#デザレックス有能"><span>デザレックス有能</span></a></h3>
<p>部屋に引きこもっていても症状は少し出る。だがこれは完全にデザレックスで抑えられる。
薬を飲むと数十分後には効果が出て、6時間くらいは目に見えて症状が治まるので、薬の効果があるのは間違いない。
また空腹時という制約が無いので使いやすい。</p>
<p>効果はビラノアの方がある気もするけれど、空腹時に飲むというのがピークに持ってくるタイミング合わせを難しくしていて、
デザレックスの方が使いやすい。</p>
<p>ただ一日一回というのは少なすぎなんじゃないかなぁ、という気もする。
明らかに翌日の同じ時間には効果がすごく少なくなっている。
なるべくピークの時間帯に一番効くように調整して乗り切ったが、
一日２回飲んじゃダメなのかなぁ。</p>
<p>なんにせよデザレックスは軽い症状に関しては良く効くので、
家から出ない生活と組み合わせると非常に有効だ。</p>
<h3 id="総括-今年はほとんど花粉症を抑えるのに成功した年だった" tabindex="-1"><a class="header-anchor" href="#総括-今年はほとんど花粉症を抑えるのに成功した年だった"><span>総括：今年はほとんど花粉症を抑えるのに成功した年だった</span></a></h3>
<p>同じフロアに住んでいる住人がめっちゃくしゃみしているのを聞いているので、
自分の対策がうまう行った年なんじゃないか、と思っている。
というか関東に住んでいて過去もっとも症状が出なかった年に思う。</p>
<p>過去にほとんど症状が出なかった年も一回くらいあったが（その年はめっちゃ飛散が少なくてピークの飛散量も少なかった記憶）、
その時でもピークの数日は辛かったので、その時以上に症状が出なかった。</p>
<p>例年もあまり外に出ないようには気をつけていたが、完全に外に出ないとこんなに違うのか、と驚く。
ただ今年は総飛散量も少なかったので、普通の年でどうなるかも見てみたい。
普通の年でもこのくらい抑えられるなら、もう花粉症の季節に沖縄とか行く必要は無いな、と思った。</p>
