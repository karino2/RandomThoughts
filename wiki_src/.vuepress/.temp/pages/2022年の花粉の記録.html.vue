<h2 id="_2022年シーズンのまとめ" tabindex="-1"><a class="header-anchor" href="#_2022年シーズンのまとめ"><span>2022年シーズンのまとめ</span></a></h2>
<p><a href="https://tenki.jp/forecaster/r_fukutomi/2022/04/18/17033.html" target="_blank" rel="noopener noreferrer">花粉シーズン終盤　関東まだ「非常に多く」飛ぶ日も　東北は大量飛散　対策いつまで(気象予報士 福冨　里香 2022年04月18日) - 日本気象協会 tenki.jp</a></p>
<p>やはり累積花粉飛散量では予報の半分くらいな地域が多く、長期予想が大きくハズレた結果になっていそうに思う。</p>
