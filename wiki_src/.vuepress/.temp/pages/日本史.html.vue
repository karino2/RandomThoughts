<p>清盛が都をあけた時を狙って義朝が上皇の身柄を確保し信西を自害に追い込む。
だがそのあと二条天皇と上皇を幽閉し、信頼が勝手気ままに振る舞う。</p>
<p>信頼は信西と対立していたが平氏には特に気にしていなかったため清盛は都に戻り、従うふりをする。</p>
<p>そして二条天皇を女装させて清盛の屋敷に逃走させて、上皇は仁和寺に避難して中立の立場となる。</p>
<p>そして清盛は天皇の命令を受けて信頼、義朝陣営を討つ。</p>
<p>義朝は長男義平を北陸に逃し、次男は傷が酷くとどめをさし、三男の頼朝は捉えられる。
義朝はかつての部下を頼るが風呂上がりを襲われ息絶える。
義平ものちにつかまり斬首される。</p>
<p>平治の乱の後、頼朝を処刑するのを清盛の継母、池禅尼が助けるように嘆願し伊豆に流す事に。</p>
<h3 id="鹿ヶ谷の陰謀-1177" tabindex="-1"><a class="header-anchor" href="#鹿ヶ谷の陰謀-1177"><span>鹿ヶ谷の陰謀（1177）</span></a></h3>
<p>後白河法皇のお気に入りだった清盛の嫁の妹、平滋子が病没すると、後白河法皇と清盛の間で対立が深まる。</p>
<p>後白河法皇の近臣たちが鹿ヶ谷に集まり、瓶子が倒れたのでへいしの首を落としてしまおう、
などといって盛り上がっていた。</p>
<p>これを告げ口された清盛は激怒し、参加者を処刑したり島流しにしたが、後白河法皇だけは自分の娘（徳子）が皇子を生むまでは手出しを控える。</p>
