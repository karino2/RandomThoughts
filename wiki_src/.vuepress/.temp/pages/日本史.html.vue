<h3 id="前九年の合戦" tabindex="-1"><a class="header-anchor" href="#前九年の合戦"><span>前九年の合戦</span></a></h3>
<p>蝦夷の人々から選ばれた代表である安倍氏が朝廷と対立し、
源頼義、源義家の親子が派遣される。</p>
<p>苦戦するが、出羽国の清原武則の助力を得て勝利する。</p>
<p>この時に負けた安倍氏側の藤原経清の子供が藤原清衡で、この時に処刑を免れて後に奥州藤原氏として権勢を誇る。</p>
<p>この時に勝利した頼義は、けれど陸奥国を手に入れる事は出来ず、助力を仰いだ清原武則が鎮守府将軍となってしまう。</p>
<h3 id="後三年の合戦" tabindex="-1"><a class="header-anchor" href="#後三年の合戦"><span>後三年の合戦</span></a></h3>
<p>前九年から20年後、白河天皇（後に院政を始める白河上皇）の時代に、清原氏の三兄弟のうちの二人がもめごとを起こしていた所に、
源義家を陸奥国の国司に任命されて、任される。</p>
<p>この三兄弟の一人が藤原清衡（この時は清原清衡を名乗っている）。もう一人を家衡と言う。</p>
<p>義家はこの揉め事の仲裁に入り裁定を行ったが、清原家衡が不満に思い、清衡の館を襲撃して妻子を殺害、
義家と清衡は反撃に出るも敗勢で撤退、翌年義家の弟、義光が参戦してなんとか勝利を収める。
これが後三年合戦。</p>
<p>後三年合戦のあと、朝廷は義家を陸奥守から解任し、さらに戦費も払わないと言い始める。
この時に義家がポケットマネーで部下に報酬を払う事で結束を強めた。</p>
<p>勢力を増した事で白河上皇は取り込む事にし、義家は正四位の下となって武士で初めて院の昇殿と許される。</p>
<h3 id="院政と武士の台頭" tabindex="-1"><a class="header-anchor" href="#院政と武士の台頭"><span>院政と武士の台頭</span></a></h3>
<p>白河上皇は藤原家などの貴族たちの力を荘園整理などで弱めて、武士などを取り立てた。
その結果武士が力をましていった。</p>
<p>僧兵の強訴に対抗するために警備を任せたのが平正盛、平忠盛親子。</p>
<h3 id="平正盛により源義親の討伐" tabindex="-1"><a class="header-anchor" href="#平正盛により源義親の討伐"><span>平正盛により源義親の討伐</span></a></h3>
<p>義家の子供の義親は対馬守（長崎）で横暴な振る舞いをし、
捉えに来た朝廷の使者を殺してしまうなどして、隠岐国に流される。
だが隠岐を出て出雲の国で暴れまわるようになり、平正盛に討伐例が出され、討伐に成功する。</p>
<p>この後義家の系譜は義忠が暗殺されて途絶える。</p>
