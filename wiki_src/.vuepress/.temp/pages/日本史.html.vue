<p>後白河天皇は二条天皇に位をゆずり上皇となる。
鳥羽法皇の側近だった信西と清盛が組んで勢いを増す。</p>
<p>それに対抗する為に義朝が選んだのが上皇に気に入られている藤原信頼（信西と対立している）。</p>
<p><strong>信西側</strong></p>
