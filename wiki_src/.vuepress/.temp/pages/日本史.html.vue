<p>近衛天皇が若くして崩御、跡継ぎが順番的には崇徳上皇の子供になるはずが、崇徳上皇の弟（雅仁親王あらため後白河天皇）に。
後白河天皇は頼長を嫌う。</p>
<p>その折、鳥羽法皇が崩御し、崇徳上皇が兵を集めて朝廷を倒すという噂が広まり、仕方なく兵を集めて乱を起こす。</p>
<p>この折、両陣営の為朝、義朝が夜討ちを進言し、上皇側は拒否して天皇方は受け入れ、天皇側が勝利する、というエピソードも。</p>
