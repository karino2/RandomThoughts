<h2 id="短所に対する批判があまり有効でない理由" tabindex="-1"><a class="header-anchor" href="#短所に対する批判があまり有効でない理由"><span>短所に対する批判があまり有効でない理由</span></a></h2>
<p>短所を指摘するのがいまいち批判として弱いのは、他の手法がもっと酷いからと思う。
結局現状は、「欠陥の多い実験経済学の手法」vs 「実際に近いが反証も証明も出来ず因果関係も分らない手法」という、どっちも酷いがどっちがマシか？という状態になっている。</p>
<p>この時に前者の欠陥を指摘するのは、特にそれが既に良く知られて議論されてる事の場合はあまり響かない。
実験経済学シンパはそれを知らないのでは無く、後者の酷さを知っている、と思っているから。
だから後者がお前が思うよりマシだ、という方が良い。
自分もだいたいにおいて後者の欠点は過小評価されていると思う。</p>
<p>自分は当初は実験経済学はあまりのもおもちゃ過ぎて大した結論は出せないのでは無いかと思っていたが、批判にこたえて実験の内容が改善されていくのを見て、批判が出来て改善出来るというのは強い性質だなぁ、と思うようにはなった。</p>
<p>社会科学全般で、主観を排するのは実験経済学も含めて出来ないとは思うけれど、見解が異なる人が合意に至るのに、実験経済学は既存の手法よりはずっとマシだとは思う。
その代わり、それで議論出来る範囲は凄く狭い。</p>
<p>既存の手法は、その不備を指摘するのは難しい事が多い。それは不備が無いのでは無く、証拠が無い事を主張しているからだ。
例えばマスクをしている国がCovid-19に感染している患者数が少ないとしても、その因果関係は分らないので、それが効果がある、と言っても、それを否定するのは難しい。効果がある事が分らないからだ。</p>
<p>実験経済学の手法で実験の問題点を批判するのは、まさに実験経済学の手法の利点を強調してしまっている。問題点を指摘出来る、というのが実験経済学の手法の強みだからだ。
もちろん実験の内容は改善されるべきだが、ある実験の問題点を指摘するだけでは「その実験よりマシな手法を示せた」事にはならない。</p>
<p>その実験を弱めたいのでは無く否定したいなら、もっとマシな物を出さないといけない。この時に実社会の相関を示すだけではあまり意味が無い。因果関係がありそうに見えると同意出来る事なら意味があるが、反論したい時はだいたい同意が無い事に対して言いたいのだから、これではあまり反論になってない。
多くのケースで見られるとか、もうちょっと正しそうにみえる事があれば否定になる事もあるけれど。</p>
<p>一番直接的な批判は欠点を改善した実験で違う結果を示す事だが、これはまさに実験経済学の土俵で戦う事になるので、実験経済学を批判したい時に取りたい方法では無い。
でも、実験経済学の手法以外でもっとマシな根拠を提出するのは、かなり難しい。</p>
<h2 id="強すぎる主張の問題点" tabindex="-1"><a class="header-anchor" href="#強すぎる主張の問題点"><span>強すぎる主張の問題点</span></a></h2>
<p>実験経済学が有害となるのは、結果が強く受け入れられ過ぎる事に思う。
制約の強い実験の結果の一般化が正しくない事があるのは明らかで、でもそれはとても頻繁に行われる。
特に政策に実際に適用されてしまうと、大きな被害を生む場合もある。</p>
<p>保守的に適応していこう、というのと、他の観察研究などの手法も併用して最終判断はしていこう、というのは、たぶん多くの実験経済学シンパの認める所と思う。けれど実際には行われていない事にも思う。</p>
<p>ただ実験経済学により制約の多い実験から過度に強い主張をしている時に、では実験経済学以外はそれをしてないのか？というのは良く考えられるべきに思う。
特に批判者が、特定の相関の観察から因果関係を主張する時は、制約が多い実験を一般化するよりもさらに酷い一般化をしている事になるが、その事には無頓着で実験経済学の側だけ一般化を否定するのはフェアじゃない。そしてこの批判は良く行われている。</p>
<p>本来は、双方が良く分らないという事を表明するべきなのだよな。
実験経済学が既存のまずいやり方に対抗する為にまずい主張をしているというのが現状では無いか。</p>
