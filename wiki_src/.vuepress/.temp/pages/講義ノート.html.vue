<h2 id="一番リアルタイムのノート取りが楽なシステムは何か" tabindex="-1"><a class="header-anchor" href="#一番リアルタイムのノート取りが楽なシステムは何か"><span>一番リアルタイムのノート取りが楽なシステムは何か？</span></a></h2>
<p>結局はここが一番重要で、今の所は全て手書きで統一が一番ラクじゃないか、と思っている。
